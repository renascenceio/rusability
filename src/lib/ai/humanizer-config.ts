/*
  humanizer-ru — редактируемая конфигурация «очеловечивания» русского ИИ-текста.
  Подход основан на open-source скилле ilyautov/humanizer-ru (MIT): каталог из
  12 категорий маркеров нейросети + список жёстких банов. Здесь — ЧИСТЫЙ модуль
  (без server-only/db), поэтому его безопасно импортировать и на клиенте
  (дашборд в /admin/humanizer), и на сервере (компиляция директивы в промпт).

  Механика: buildHumanizerDirective() собирает из включённых категорий, банов и
  переключателей единый блок-инструкцию, который добавляется к системному
  промпту ВСЕХ генераций (см. buildRequirementsPreamble в model.ts). Опционально
  включается второй проход-переписывание (humanizeBlocks в humanizer.ts).
*/

export type HumanizerStrength = "light" | "balanced" | "aggressive";
export type HumanizerArea = "articles" | "news";

export interface HumanizerConfig {
  /** Мастер-выключатель всей системы очеловечивания. */
  enabled: boolean;
  /** Насколько агрессивно переписывать под живой голос. */
  strength: HumanizerStrength;
  /** К каким областям генерации применять директиву. */
  applyTo: Record<HumanizerArea, boolean>;
  /** Включённые категории маркеров (ключи A…L). */
  categories: Record<string, boolean>;
  /** Жёсткие баны — конструкции с абсолютным запретом. */
  hardBans: string[];
  /** Вычищать длинные тире «—» (главный типографический маркер ИИ). */
  stripEmDash: boolean;
  /** Возвращать живые частицы (же, ведь, вот, разве, неужели). */
  addParticles: boolean;
  /** Рвать ритм: чередовать короткие и длинные предложения (burstiness). */
  boostBurstiness: boolean;
  /** Контрастное вычитание: заменять самое предсказуемое слово в предложении. */
  contrastiveSubtraction: boolean;
  /** Второй проход: переписать готовый черновик, убрав оставшиеся маркеры. */
  secondPass: boolean;
  /** Дополнительные правила редакции (свободный текст, добавляется в конец). */
  extraNotes: string;
}

export interface HumanizerCategory {
  /** Буква-код из каталога humanizer-ru (A…L). */
  id: string;
  /** Короткое название группы. */
  title: string;
  /** Что ловит эта категория (для UI). */
  summary: string;
  /** Инструкция модели: что делать, чтобы убрать эти маркеры. */
  directive: string;
}

