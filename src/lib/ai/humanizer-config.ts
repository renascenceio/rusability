/**
 * Compile the enabled config into a single Russian system-prompt block.
 * Returns "" when disabled — callers simply skip it.
 */
export function buildHumanizerDirective(cfg: HumanizerConfig): string {
  if (!cfg.enabled) return "";

  const lines: string[] = [
    "ОЧЕЛОВЕЧИВАНИЕ ТЕКСТА (humanizer-ru): пиши так, чтобы текст читался как живая человеческая речь, а не как генерация нейросети. Убирай перечисленные ниже маркеры прямо на этапе написания.",
    STRENGTH_FRAMING[cfg.strength],
  ];

  const activeCats = HUMANIZER_CATEGORIES.filter((c) => cfg.categories[c.id]);
  if (activeCats.length) {
    lines.push("", "УБИРАЙ ЭТИ КЛАССЫ МАРКЕРОВ:");
    for (const c of activeCats) {
      lines.push(`- [${c.id}] ${c.title}. ${c.directive}`);
    }
  }

  if (cfg.hardBans.length) {
    lines.push(
      "",
      "ЖЁСТКИЕ БАНЫ (не употребляй эти конструкции ни в каком виде):",
      cfg.hardBans.map((b) => `«${b}»`).join("; ") + ".",
    );
  }

  const toggles: string[] = [];
  if (cfg.stripEmDash)
    toggles.push(
      "Не используй длинные тире «—» — это главный типографический маркер ИИ; перестраивай фразу или ставь обычный дефис/двоеточие.",
    );
  if (cfg.addParticles)
    toggles.push(
      "Возвращай живые частицы русской речи (же, ведь, вот, разве, неужели) там, где это естественно.",
    );
  if (cfg.boostBurstiness)
    toggles.push(
      "Рви ритм: чередуй короткие рубленые и длинные развёрнутые предложения, не держи одинаковую длину подряд.",
    );
  if (cfg.contrastiveSubtraction)
    toggles.push(
      "Избегай самых предсказуемых, «ожидаемых» слов — выбирай более точное и менее шаблонное слово там, где это уместно.",
    );
  if (toggles.length) {
    lines.push("", "ДОПОЛНИТЕЛЬНО:");
    for (const t of toggles) lines.push(`- ${t}`);
  }

  const notes = cfg.extraNotes.trim();
  if (notes) lines.push("", "ПРАВИЛА РЕДАКЦИИ:", notes);

  return lines.join("\n");
}
