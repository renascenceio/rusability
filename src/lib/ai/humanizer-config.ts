export const DEFAULT_HUMANIZER: HumanizerConfig = {
  enabled: true,
  strength: "balanced",
  applyTo: { articles: true, news: false },
  categories: Object.fromEntries(HUMANIZER_CATEGORIES.map((c) => [c.id, true])),
  hardBans: DEFAULT_HARD_BANS,
  stripEmDash: true,
  addParticles: true,
  boostBurstiness: true,
  contrastiveSubtraction: false,
  secondPass: false,
  extraNotes: "",
};

/** Merge a stored partial (possibly older shape) over the defaults. */
export function mergeHumanizer(partial: Partial<HumanizerConfig> | null | undefined): HumanizerConfig {
  const base = DEFAULT_HUMANIZER;
  const p = partial ?? {};
  return {
    ...base,
    ...p,
    applyTo: { ...base.applyTo, ...(p.applyTo ?? {}) },
    // Keep every known category present even if the stored blob predates it.
    categories: { ...base.categories, ...(p.categories ?? {}) },
    hardBans: Array.isArray(p.hardBans) ? p.hardBans : base.hardBans,
  };
}

